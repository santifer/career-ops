#!/usr/bin/env node
// scripts/compute-next-moves.mjs
// CLI runner for the Next Moves synthesis layer. Reads all the existing
// career-ops state (tracker, outreach, network, research queue, liveness,
// profile) and emits a ranked action list to:
//
//   data/next-moves.json   — machine-readable; baked into the dashboard build
//   data/next-moves.md     — human-readable; included in heartbeat email
//
// Run manually:
//   node scripts/compute-next-moves.mjs
//   node scripts/compute-next-moves.mjs --top 3 --skip 5
//   node scripts/compute-next-moves.mjs --dry-run
//
// Scheduled: add a launchd plist that runs every 4 hours OR right before
// the heartbeat email job, so the surfaced moves are always fresh.

import { writeFileSync } from 'fs';
import { join } from 'path';
import { fileURLToPath } from 'url';
import { loadNextMovesInputs } from '../lib/next-moves-inputs.mjs';
import { computeNextMoves } from '../lib/next-moves.mjs';

const ROOT = fileURLToPath(new URL('..', import.meta.url));

function parseFlags() {
  const args = process.argv.slice(2);
  const flags = { topN: 5, skipN: 8, dryRun: false };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--top') flags.topN = parseInt(args[++i], 10) || 5;
    else if (a === '--skip') flags.skipN = parseInt(args[++i], 10) || 8;
    else if (a === '--dry-run') flags.dryRun = true;
  }
  return flags;
}

function renderMarkdown(result) {
  const d = result.deadline_stats;
  const today = new Date().toISOString().slice(0, 10);
  let md = `# Next Moves — ${today}\n\n`;
  md += `_Deadline ${d.deadline_iso} · **${d.days_left} days** left · `;
  md += `${d.apps_applied} applied of ~${d.apps_applied + d.apps_needed_estimate} needed · `;
  md += `${d.apps_per_week_required}/wk required_\n\n`;

  if (result.top_moves.length === 0) {
    md += `_No actions ranked above threshold. Run a scan or evaluate something._\n`;
    return md;
  }

  md += `## Top ${result.top_moves.length} actions this week\n\n`;
  for (const m of result.top_moves) {
    md += `### ${m.rank}. ${m.label}\n`;
    md += `**Cost:** ~${m.cost_hours}h &middot; **Impact:** ${m.impact_score} &middot; `;
    md += `**Composite:** ${m.composite_score}\n\n`;
    md += `${m.evidence}\n\n`;
  }

  if (result.skip_list.length) {
    md += `## Skip this week (${result.skip_list.length})\n\n`;
    md += `_Things that look actionable but fail a hard gate or score below the threshold._\n\n`;
    for (const s of result.skip_list) {
      md += `- **${s.label}** — ${s.reason}\n`;
    }
    md += `\n`;
  }

  md += `---\n_Generated by scripts/compute-next-moves.mjs &middot; `;
  md += `${result.scoring_notes.candidate_count} candidates considered &middot; `;
  md += `formula: \`${result.scoring_notes.formula}\`_\n`;
  return md;
}

// ── Run ───────────────────────────────────────────────────────────────────

const flags = parseFlags();
const inputs = loadNextMovesInputs();
const result = computeNextMoves({ ...inputs, topN: flags.topN, skipN: flags.skipN });

const jsonOut = join(ROOT, 'data/next-moves.json');
const mdOut = join(ROOT, 'data/next-moves.md');

if (flags.dryRun) {
  console.log('[next-moves] dry-run — would write:');
  console.log('  ' + jsonOut);
  console.log('  ' + mdOut);
  console.log('[next-moves] top ' + result.top_moves.length + ' actions:');
  for (const m of result.top_moves) {
    console.log('  ' + m.rank + '. [' + m.composite_score + '] ' + m.label);
  }
} else {
  writeFileSync(jsonOut, JSON.stringify(result, null, 2));
  writeFileSync(mdOut, renderMarkdown(result));
  console.log('[next-moves] wrote ' + jsonOut);
  console.log('[next-moves] wrote ' + mdOut);
  console.log('[next-moves] top action: ' + (result.top_moves[0]?.label || '(none)'));
  console.log('[next-moves] deadline: ' + result.deadline_stats.days_left + ' days · ' + result.deadline_stats.apps_per_week_required + '/wk required');
}
