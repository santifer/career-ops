#!/usr/bin/env node
// scripts/company-research-worker.mjs
// Reads data/company-research-queue/*.json and emits a daily-research-todo
// markdown that the heartbeat email + dashboard sidebar can surface. Does NOT
// invoke the researcher agent itself — that needs an interactive Claude Code
// session (runtime-bridge gap documented in build-dashboard.mjs L13280).
//
// What it does:
//   1. Aggregates queue files by slug, dedupes sections per company.
//   2. Writes data/company-research-todo.md (consumed by heartbeat + sidebar).
//   3. Writes data/company-research-todo.json (machine-readable).
//   4. Optionally archives queue files older than --archive-days to
//      data/company-research-queue/processed/ so the active set stays small.
//
// Flags:
//   --archive-days N       move queue files updated > N days ago to processed/
//                          (default: never; the worker is idempotent without)
//   --dry-run              print summary but write nothing
//
// Surfaced in:
//   - data/company-research-todo.md  (markdown, human + LLM readable)
//   - data/company-research-todo.json (count, by_section, by_slug)

import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync, statSync, renameSync } from 'fs';
import { join } from 'path';
import { fileURLToPath } from 'url';

const ROOT = fileURLToPath(new URL('..', import.meta.url));
const QUEUE_DIR = join(ROOT, 'data/company-research-queue');
const PROCESSED_DIR = join(QUEUE_DIR, 'processed');
const TODO_MD = join(ROOT, 'data/company-research-todo.md');
const TODO_JSON = join(ROOT, 'data/company-research-todo.json');

function parseFlags() {
  const args = process.argv.slice(2);
  const flags = { archiveDays: null, dryRun: false };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--archive-days') { flags.archiveDays = parseInt(args[++i], 10) || null; }
    else if (a === '--dry-run') { flags.dryRun = true; }
  }
  return flags;
}

function loadQueue() {
  if (!existsSync(QUEUE_DIR)) return [];
  return readdirSync(QUEUE_DIR)
    .filter(f => f.endsWith('.json'))
    .map(f => {
      const fp = join(QUEUE_DIR, f);
      try {
        const stat = statSync(fp);
        if (!stat.isFile()) return null;
        const data = JSON.parse(readFileSync(fp, 'utf-8'));
        return { _file: f, _fp: fp, _mtime: stat.mtimeMs, ...data };
      } catch (_) { return null; }
    })
    .filter(Boolean);
}

function dedupeSections(sections) {
  // Keep most-recent ts per section name
  const byName = new Map();
  for (const s of sections || []) {
    const prev = byName.get(s.section);
    if (!prev || (s.ts || '') > (prev.ts || '')) byName.set(s.section, s);
  }
  return Array.from(byName.values()).sort((a, b) => (a.section || '').localeCompare(b.section || ''));
}

function buildSummary(queue) {
  const today = new Date().toISOString().slice(0, 10);
  const companies = queue
    .map(q => ({
      slug: q.slug,
      sections: dedupeSections(q.sections),
      created_at: q.created_at,
      updated_at: q.updated_at,
    }))
    .filter(c => c.slug && c.sections.length)
    .sort((a, b) => (b.updated_at || '').localeCompare(a.updated_at || ''));

  const bySection = {};
  for (const c of companies) {
    for (const s of c.sections) {
      bySection[s.section] = (bySection[s.section] || 0) + 1;
    }
  }

  let md = `# Company Research Queue — ${today}\n\n`;
  md += `_Generated by scripts/company-research-worker.mjs. Companies + sections queued via the dashboard's Refresh / Refresh-all buttons._\n\n`;
  if (!companies.length) {
    md += `**Queue empty.** No companies awaiting research.\n`;
  } else {
    md += `**${companies.length}** companies awaiting research &middot; **${Object.values(bySection).reduce((a, b) => a + b, 0)}** total section requests\n\n`;
    md += `## By section\n`;
    for (const [section, n] of Object.entries(bySection).sort((a, b) => b[1] - a[1])) {
      md += `- \`${section}\` — ${n} companies\n`;
    }
    md += `\n## By company\n`;
    for (const c of companies) {
      md += `### ${c.slug}\n`;
      md += `_Updated: ${c.updated_at || '—'}_\n\n`;
      for (const s of c.sections) {
        md += `- \`${s.section}\` (last requested: ${(s.ts || '').slice(0, 10) || '—'})\n`;
      }
      md += `\n**Action:** \`claude -p '/researcher Compile current research on ${c.slug} for sections: ${c.sections.map(s => s.section).join(', ')}. Use the council if needed.'\`\n\n`;
    }
  }

  const json = {
    generated_at: new Date().toISOString(),
    total_companies: companies.length,
    total_requests: Object.values(bySection).reduce((a, b) => a + b, 0),
    by_section: bySection,
    companies,
  };

  return { md, json };
}

function archive(queue, days) {
  if (!days) return 0;
  const cutoff = Date.now() - days * 24 * 60 * 60 * 1000;
  let n = 0;
  if (!existsSync(PROCESSED_DIR)) mkdirSync(PROCESSED_DIR, { recursive: true });
  for (const q of queue) {
    if (q._mtime < cutoff) {
      try {
        renameSync(q._fp, join(PROCESSED_DIR, q._file));
        n++;
      } catch (_) {}
    }
  }
  return n;
}

const flags = parseFlags();
const queue = loadQueue();
const { md, json } = buildSummary(queue);

if (flags.dryRun) {
  console.log(`[dry-run] ${queue.length} queue file(s) — would write ${md.length} bytes to ${TODO_MD}`);
} else {
  writeFileSync(TODO_MD, md);
  writeFileSync(TODO_JSON, JSON.stringify(json, null, 2));
  const archived = archive(queue, flags.archiveDays);
  console.log(`[research-worker] queued: ${json.total_companies} companies / ${json.total_requests} section requests`);
  console.log(`[research-worker] wrote ${TODO_MD}`);
  console.log(`[research-worker] wrote ${TODO_JSON}`);
  if (archived) console.log(`[research-worker] archived ${archived} stale queue file(s) to ${PROCESSED_DIR}`);
}
